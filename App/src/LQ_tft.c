void TFTSPI_WriteCommand(uint8_t dat)			//写命令
{
    TFT_DC(0);
#if SIM_SPI
	SPI_enable(SPI_ENABLE);
	SPI_write(dat);
	SPI_enable(SPI_DISABLE);
#else
	spi_mosi(spi0,SPI_PCS0,&dat,&dat,1);
#endif
}

void TFTSPI_WriteData(uint8_t dat)			//写数据
{
    TFT_DC(1);
#if SIM_SPI
	SPI_enable(SPI_ENABLE);
	SPI_write(dat);
	SPI_enable(SPI_DISABLE);
#else
	spi_mosi(spi0,SPI_PCS0,&dat,&dat,1);
#endif
}

void TFTSPI_WriteData_16Bit(uint16_t dat)	//向液晶屏写一个16位数据
{
    uint8_t h,l;
    h = dat >> 8;
    l = (uint8_t)dat;

    TFT_DC(1);

#if SIM_SPI
	SPI_enable(SPI_ENABLE);
	SPI_write(h);
	SPI_write(l);
	SPI_enable(SPI_DISABLE);
#else
	spi_mosi(spi0,SPI_PCS0,&h,&h,1); 	//写入高8位数据
    spi_mosi(spi0,SPI_PCS0,&l,&l,1);	//写入低8位数据
#endif
}

