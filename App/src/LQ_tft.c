/**********************************************************
函数名称：TFTSPI_Set_Pos()
入口参数：起始终止坐标
出口参数：无
时间：2018/1/26
功能说明：重新定位输入信息位置
其他说明：无
**********************************************************/
void TFTSPI_Set_Pos(uint8_t xs,uint8_t ys,uint8_t xe,uint8_t ye)
{
  TFTSPI_WriteCommand(0x2A);      	//Colulm addRSTs set
  TFTSPI_WriteData(0x00);		//行起始坐标高8位，始终为零
  TFTSPI_WriteData(xs);			//行起始坐标低8位
  TFTSPI_WriteData(0x00);		//行终止坐标高8位，始终为零
  TFTSPI_WriteData(xe);			//行终止坐标低8位
  TFTSPI_WriteCommand(0x2B);      	//Colulm addRSTs set
  TFTSPI_WriteData(0x00);		//列起始坐标高8位，始终为零
  TFTSPI_WriteData(ys);			//列起始坐标低8位
  TFTSPI_WriteData(0x00);		//列终止坐标高8位，始终为零
  TFTSPI_WriteData(ye);			//列终止坐标低8位
  TFTSPI_WriteCommand(0x2C);      	//GRAM接收MCU数据或命令
}

