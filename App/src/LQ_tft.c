//  起始点x,起始点y,频谱宽度，高度，前景色，背景色
void ColumnarSetting(uint8_t x,uint8_t y, uint8_t w,uint8_t h,uint16_t fclolr,uint16_t bcolor)
{
  Start_X=x;
  Start_Y=y;

  AreaW = w;  //AreaW = Colum_W*ColumnarMAX*2;
  AreaH = h;  //AreaY = Colum_H;

  Colum_W = AreaW/(ColumnarMAX*2);     //TFT18W/40;
  Colum_H = AreaH;                     //TFT18H/2;

  BColor = bcolor;
  FColor = fclolr;
}

void DrawingColumnar(uint8_t t,uint8_t duty,uint8_t cl)
{
  unsigned int i,j;

  //上半截
  TFTSPI_Set_Pos(Start_X+Colum_W*t,Start_Y,Start_X+Colum_W*(t+1),(uint8_t)(Start_Y+Colum_H*(1-duty*0.01)));
  for(i=0;i<Colum_W+2;i++)      //160
  {
    for(j=0;j<Colum_H+2;j++)
    {
      TFTSPI_WriteData_16Bit(BColor);
    }
  }
  //下半截
  TFTSPI_Set_Pos(Start_X+Colum_W*t,(uint8_t)(Start_Y+Colum_H*(1-duty*0.01)),Start_X+Colum_W*(t+1),Start_Y+Colum_H);
  for(i=0;i<Colum_W+2;i++)      //160
  {
    for(j=0;j<Colum_H+2;j++)
    {
      TFTSPI_WriteData_16Bit(FColor);
    }
  }

  if(cl)
  {
    //补充空隙
    TFTSPI_Set_Pos(Start_X+Colum_W*(t+1)+1,Start_Y,Start_X+Colum_W*(t+2),Start_Y+Colum_H);
    for(i=0;i<Colum_W;i++)      //160
    {
      for(j=0;j<Colum_H+2;j++)
      {
        TFTSPI_WriteData_16Bit(BColor);
      }
    }
  }
}

